        public static string UserAgentRamdom
        {
            get
            {
                return _userAgents[_random.Next(5)];
            }
        }

        public static string GetRandomHexClientId()
        {
            var rnd = _random.Next(20) * 21474838;
            string hexValue = rnd.ToString("X");
            return hexValue;
        }
